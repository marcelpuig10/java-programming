package day10_shortland_operators;

public class ShortHandOperators {
    public static void main(String[] args) {
        int cars = 10;
        System.out.println("cars = " + cars);
        cars = cars +2;
        System.out.println("cars = " + cars);
        cars +=5;
        System.out.println("cars = " + cars);

        //6 people left

        cars -=6;
        System.out.println("cars = " + cars);

        char letter ='A';
        System.out.println("letter = " + letter);
        letter+=3;
        System.out.println("letter = " + letter);
        letter+=1;
        System.out.println("letter = " + letter);

    }
}
