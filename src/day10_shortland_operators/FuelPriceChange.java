package day10_shortland_operators;

public class FuelPriceChange {
    public static void main(String[] args) {
        int count = 3;
        System.out.println("count = " + count);

        count+=2;
        System.out.println("count = " + count);

        count -=1;
        System.out.println("count = " + count);

        count *=5;
        System.out.println("count = " + count);

        count /=5;
        System.out.println("count = " + count);
    }
}
