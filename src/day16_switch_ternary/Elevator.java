package day16_switch_ternary;

public class Elevator {

    public static void main(String[] args) {
        int floorNum = 1;
        if (floorNum == 1) {
            System.out.println("Floor 1");
        }else if (floorNum == 2){
            System.out.println("Floor 2");
        }else if (floorNum == 3){
            System.out.println("Floor 3");
        }else {
            System.out.println("Invalid floor");
        }
    }
}
