package day08_casting_scanner;

public class CastingExamples {
    public static void main(String[] args) {
        //byte-short-int-long
        byte num1=100;
        short num2=num1;
        int num3=num1;
        int num4=1000;
        long num5=num4;
        float num6=124.4F;
        double num7=num6;
        int num8=3456;
        double num9=num8;
        System.out.println(num8);
        System.out.println("num9 = " + num9);


    }
}
