package day14_multi_branch_if_statements;

public class ChooseLanguage {
    public static void main(String[] args) {
        System.out.println("Choose your language");
        int selection =3 ;
        if (selection==1) {
            System.out.println("Hello, thank you for your call");
        }else if(selection==2){
            System.out.println("Hola, gracias por tu llamada");
        }else if (selection==3){
            System.out.println("merhaba, skjdaskdahdha");
        }else if (selection==4){
            System.out.println("Salut, merci pour votre appel");
        }else if (selection==5){
            System.out.println("kjsldjflsjlfdjsljf");
        }else{
            System.out.println("bye");
        }

    }
}
