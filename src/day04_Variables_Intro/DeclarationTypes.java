package day04_Variables_Intro;

public class DeclarationTypes {
    public static void main(String[] args) {
        //Declaration + assignment
        int bananas=34;

        System.out.println(bananas);

        int students=400;
        System.out.println(students);

        students=401;
        System.out.println(students);

        int peopleInCity, birdsOntTree, fishInLake;
        peopleInCity=1000;
        birdsOntTree=21;
        fishInLake=100;

        System.out.println(peopleInCity);
        System.out.println(birdsOntTree);
        System.out.println(fishInLake);

        int studentsInNY=25, studentsInVA=200;
        System.out.println(studentsInNY);
        System.out.println(studentsInVA);



    }
}
