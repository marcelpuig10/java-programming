package day05_primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args) {
        String brand= "Apple";
        String color="Gold";
        int price= 599;
        boolean waterResist= true;
        boolean smart= true;
        char gender= 'F';


        System.out.println("Brand of watch: \t"+brand);
        System.out.println("Color: \t\t\t\t"+color);
        System.out.println("Price: \t\t\t\t$"+price);
        System.out.println("Water Resistant: \t"+waterResist);
        System.out.println("Smart: \t\t\t\t"+smart);
        System.out.println("Gender: \t\t\t"+gender);
    }
}
